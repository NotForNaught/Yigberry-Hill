package yigberryHill;

public class InteractiveThing {
	
	private int x;
	private int y;
	private String message;

	public InteractiveThing(int x, int y, String message) {
		this.x = x;
		this.y = y;
		this.message = message;
	}

	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}

	public String getMessage() {
		return message;
	}	

}
